
    <h1 class="cover-heading">Compact &amp; Fast MVC Framework</h1>

        SimpleMVC is a straightforward PHP implementation of the MVC pattern. It has no dependencies and is easy to use.<br>

    <hr/>


        <h3>1. Prerequisites</h3>
        <ul>
            <li>A Webserver with <a href="http://php.net" target="_blank">PHP 7.0</a> or higher and the possibility to rewrite a URL
                (<a href="http://iis.net" target="_blank">IIS</a>,
                <a href="https://httpd.apache.org/" target="_blank">Apache</a>, Nginx, etc.)</li>
            <li><a href="http://getcomposer.org" target="_blank">Composer</a> for PSR-4 pre-loading and (if you want) package management for your project.</li>
        </ul>

        <h3>2. Download SimpleMVC</h3>
        <a href="simplemvc-1.0.0.zip"><div class="btn btn-default">Get SimpleMVC version 1.0.0</div></a>
        <p>Simply copy and paste the content of the ZIP file to your project root directory.</p>
        <p>Run <code>composer update</code> to generate the needed PSR-4 preloading files.</p>

        <h3>3. Setup your Webserver (optional)</h3>
        <p>In the zip file there is a .htaccess file included. It redirects all requests to the index.php file.If you don't use Apache you will need to make these changes manually.</p>

        <h3>4. Let's go!</h3>
        <p>Experiment with the default setup or check the docs for more information. Happy coding! </p>


